    private void cb6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb6ActionPerformed
       String jdbc_driver = "com.mysql.jdbc.Driver";
            String user = "root";
            String pass = "";
            String url = "jdbc:mysql://localhost:3306/dbpegawai";
            File reportFile= new File("");
            String dirr = "";
            
            try {
             Class.forName(jdbc_driver);
             Connection conn = DriverManager.getConnection(url, user, pass);
             Statement stat = conn.createStatement();
             String sql = "SELECT * FROM tbgaji ";
             dirr = reportFile.getCanonicalPath() + "/src/datapegawai/";
             JasperDesign design = JRXmlLoader.load(dirr + "report2.jrxml");
             JasperReport jr = JasperCompileManager.compileReport(design);
             ResultSet rs = stat.executeQuery(sql);
             JRResultSetDataSource rsDataSource = new JRResultSetDataSource(rs);
             JasperPrint jp = JasperFillManager.fillReport(jr, new HashMap(), 
                     rsDataSource);
             JasperViewer.viewReport(jp);
             
         } catch (ClassNotFoundException | SQLException | IOException | JRException ex) {
    //             Logger.getLogger(DataPegawai.class.getName()).log(Level.SEVERE, null, ex);
             JOptionPane.showMessageDialog(null, "\nGagal Mencetak\n" + ex,
                     "Kesalahan", JOptionPane.ERROR_MESSAGE);
         
         }           
    }//GEN-LAST:event_cb6ActionPerformed

